    private void leerBoton(String boton){
    
        if(boton.equals("arte")){
            if(nBoton == 1){
            nBoton = 0;
            }
            }else
            if(boton.equals("square")){
            if(nBoton == 2){
            nBoton = 0;
            }
        nBoton = 2;
        }
    
    
    }
    
    
    
    private void miraBoton(JButton botonPulsado){
    leerBoton(botonPulsado.getText());
    }

